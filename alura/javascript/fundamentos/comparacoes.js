// == (comparação implicita)
// compara os valores das variáveis
const numero = 5
const texto = "5"

console.log(numero == texto)


// === (comparação explicita)
// compara os valores e os tipos das variáveis
console.log(numero === texto)

// mostra o tipo da variáveil
console.log(typeof numero)
console.log(typeof texto)


